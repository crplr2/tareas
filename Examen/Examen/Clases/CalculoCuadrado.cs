using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Examen.Clases
{
    //Clase para calcular el area del cuadrado
    class CalculoCuadrado
    {
        public double perimetro(double lado)
        {
            return 4*lado;
        }

        public double area(double lado)
        {
            return lado * lado;
        }
    }
